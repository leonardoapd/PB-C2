    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            String persona = cBoxTipoPersonaBuscar.getSelectedItem().toString();
            String nombre = textFieldNombreBuscar.getText();
            String direccion = textFieldDireccionBuscar.getText();
            String telefono = textFieldTelefonoBuscar.getText();
            String correo = textFieldCorreoBuscar.getText();
            String ciudad = textFieldCiudadBuscar.getText();
            String departamento = textFieldDepartamentoBuscar.getText();
            String nroDocumento = textFieldNoDocumentoBuscar.getText();
            String fechaNacimiento = textFieldFechaBuscar.getText();
            int id = Integer.parseInt(textFieldIDBuscar.getText());
            String tipoDocumento = "";

            if (rButtonCCBuscar.isSelected()) {
                tipoDocumento = "C.C";
            } else if (rButtonNitBuscar.isSelected()) {
                tipoDocumento = "NIT";
            } else if (rButtonOtroBuscar.isSelected()) {
                tipoDocumento = "Otro";
            }
            switch (persona) {
                case "Cliente" -> {
                    boolean clienteActualizado = false;
                    try {
                        clienteActualizado = controlCliente.actualizar(nombre, direccion, telefono, correo,
                                ciudad, departamento, tipoDocumento, Integer.parseInt(nroDocumento), fechaNacimiento,
                                id);
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, "Cliente no existe.");
                    }

                    if (clienteActualizado) {
                        JOptionPane.showMessageDialog(this, "El cliente ha sido actualizado exitosamente.");
                    }
                    limpiarCampos(2);
                }
                case "Vendedor" -> {
                    boolean vendedorActualizado = false;
                    try {
                        vendedorActualizado = controlVendedor.actualizar(nombre, direccion, telefono, correo, ciudad, departamento,
                                tipoDocumento, Integer.parseInt(nroDocumento), id);
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, "Vendedor no existe.");
                    }

                    if (vendedorActualizado) {
                        JOptionPane.showMessageDialog(this, "El vendedor ha sido actualizado exitosamente.");
                    }
                    limpiarCampos(2);
                }
                case "Proveedor" -> {
                    boolean proveedorActualizado = false;
                    try {
                        proveedorActualizado = controlProveedor.actualizar(nombre, direccion, telefono,
                                correo, ciudad, departamento, tipoDocumento, Integer.parseInt(nroDocumento), id);

                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, "El proveedor no existe.");
                    }

                    if (proveedorActualizado) {
                        JOptionPane.showMessageDialog(this, "El proveedor ha sido actualizado exitosamente.");
                    }
                    limpiarCampos(2);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Por favor rellene todos los campos.");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            int id = Integer.parseInt(textFieldIDBuscar.getText());
            String persona = cBoxTipoPersonaBuscar.getSelectedItem().toString();

            var matrizDevuelta = switch (persona) {
                case "Cliente" ->
                    controlCliente.buscar(id);

                case "Vendedor" ->
                    controlVendedor.buscar(id);

                case "Proveedor" ->
                    controlProveedor.buscar(id);
                default ->
                    null;
            };
            if (matrizDevuelta.length > 9) {
                textFieldFechaBuscar.setText(matrizDevuelta[9]);
            }
            if (matrizDevuelta != null) {
                textFieldIDBuscar.setText(matrizDevuelta[0]);
                textFieldNombreBuscar.setText(matrizDevuelta[1]);
                textFieldDireccionBuscar.setText(matrizDevuelta[2]);
                textFieldTelefonoBuscar.setText(matrizDevuelta[3]);
                textFieldCorreoBuscar.setText(matrizDevuelta[4]);
                textFieldCiudadBuscar.setText(matrizDevuelta[5]);
                textFieldDepartamentoBuscar.setText(matrizDevuelta[6]);
                textFieldNoDocumentoBuscar.setText(matrizDevuelta[8]);
                String tipo = matrizDevuelta[7];
                switch (tipo) {
                    case "C.C" ->
                        rButtonCCBuscar.setSelected(true);
                    case "NIT" ->
                        rButtonNitBuscar.setSelected(true);
                    case "Otro" ->
                        rButtonOtroBuscar.setSelected(true);
                }
            } else {
                JOptionPane.showMessageDialog(this, "No se ha encontrado informacion.");
                limpiarCampos(2);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se ha podido realizar la busqueda, intente nuevamente.");
            limpiarCampos(2);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

