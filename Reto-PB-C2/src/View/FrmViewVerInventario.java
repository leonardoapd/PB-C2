    public void refrescarInventario() {
        //Se establecen los elementos de la cabecera de la tabla
        String[] cabeceraProducto = {"ID", "Nombre", "Unidad de Medida", "Precio", "Cantidad", "ID Proveedor", "Descripcion"};
        //Se recogen los datos de los clientes aÃ±adidos en el almacenamiento temporal
        String[][] matrizProducto = controlInventario.refrescarTablaInventario();
        //Se agregan los datos y la cabecera de la tabla al modelo
        modeloTablaProductos.setDataVector(matrizProducto, cabeceraProducto);
        //Se agrega el modelo a la vista
        tableInventario.setModel(modeloTablaProductos);
        if (tableInventario.getColumnModel().getColumnCount() > 0) {
            tableInventario.getColumnModel().getColumn(0).setPreferredWidth(30);
            tableInventario.getColumnModel().getColumn(1).setPreferredWidth(200);
            tableInventario.getColumnModel().getColumn(6).setPreferredWidth(300);
        }
    }

